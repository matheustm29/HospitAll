    public void delEst() {
        try {
            int cpf = Integer.parseInt(cxCpf.getText().trim());
            int resposta = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja deletar o estagi√°rio com CPF " + cpf + "?", "Confirma√ß√£o", JOptionPane.YES_NO_OPTION);
            
            if (resposta == JOptionPane.YES_OPTION) {
                EstagiarioDAO estagiarioDAO = new EstagiarioDAO();
                boolean sucesso = estagiarioDAO.deleteByCpf(cpf);
                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Estagi√°rio deletado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(this, "Estagi√°rio com CPF " + cpf + " n√£o encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O CPF deve ser um n√∫mero v√°lido.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao deletar estagi√°rio: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void consEst(){
        try {
            int cpf = Integer.parseInt(cxCpf.getText().trim());
            EstagiarioDAO estagiarioDAO = new EstagiarioDAO();
            Estagiario est = estagiarioDAO.findByCpf(cpf);

            if (est != null) {
                String dados = String.format(
                    "DADOS DO ESTAGI√ÅRIO:\n---------------------------\n" +
                    " - Nome: %s\n - CPF: %d\n - Idade: %d\n" +
                    " - Institui√ß√£o: %s\n - Per√≠odo: %d",
                    est.getNome(), est.getCpf(), est.getIdade(),
                    est.getInstituicaoEnsino(), est.getPeriodo()
                );
                JOptionPane.showMessageDialog(this, dados, "Consulta de Estagi√°rio", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Estagi√°rio com CPF " + cpf + " n√£o encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O CPF deve ser um n√∫mero v√°lido.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar estagi√°rio: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
        
    
    
    public void altEst() {
        try {
            int cpf = Integer.parseInt(cxCpf.getText().trim());
            String novoPeriodoStr = JOptionPane.showInputDialog(this, "Digite o novo per√≠odo para o estagi√°rio com CPF " + cpf + ":");
            
            if (novoPeriodoStr != null && !novoPeriodoStr.trim().isEmpty()) {
                int novoPeriodo = Integer.parseInt(novoPeriodoStr);
                EstagiarioDAO estagiarioDAO = new EstagiarioDAO();
                boolean sucesso = estagiarioDAO.updatePeriodo(cpf, novoPeriodo);
                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Per√≠odo atualizado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Estagi√°rio com CPF " + cpf + " n√£o encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "CPF e Per√≠odo devem ser n√∫meros.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao alterar per√≠odo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

            
